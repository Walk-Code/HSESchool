package com.zhuochuang.hsej.entity;

public class OneCardSolutionEntity {
	private String id;
	private String xh;
	private long createDate;
	private double oddFare;
	private double opFare;
	private String describe;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getXh() {
		return xh;
	}

	public void setXh(String xh) {
		this.xh = xh;
	}

	public long getCreateDate() {
		return createDate;
	}

	public void setCreateDate(long createDate) {
		this.createDate = createDate;
	}

	public double getOddFare() {
		return oddFare;
	}

	public void setOddFare(double oddFare) {
		this.oddFare = oddFare;
	}

	public double getOpFare() {
		return opFare;
	}

	public void setOpFare(double opFare) {
		this.opFare = opFare;
	}

	public String getDescribe() {
		return describe;
	}

	public void setDescribe(String describe) {
		this.describe = describe;
	}

}
